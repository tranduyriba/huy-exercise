// 1. abs() => Trả về giá trị tuyệt đối

console.log(Math.abs(-10));

// 2. ceil() => Luôn làm tròn lên

console.log(Math.ceil(3.0))

// 3. floor() => Luôn làm tròn xuống

console.log(Math.floor(4.8))

// 4. round() => Làm tròn theo toán học, dưới 3.5 làm tròn xuống. Trên 3.5 làm tròn lên

console.log(Math.round(4.6))

// 5. Math.random()

console.log(Math.random()); // Luôn trả về 1 số random 0 < a < 1

// 6. min(), max()

console.log(Math.min(1, 2, 3))